# Write a method called count_vowels takes a string as input, and returns the number of vowels in the string. Assume that all the letters are lower case.

# vowels = ['a', 'e', 'i', 'o', 'u']

# Hint: methods to use: include?
def count_vowels(str)
  str.scan(/[aeoui]/).count
end

p count_vowels("qwrty") == 0
p count_vowels("abcd")==1
p count_vowels("colour")==3
p count_vowels("hey whats your name")==6
